package quiz;

public class Calc {
	
	 public static int plus(int a, int b) {
		 int sum = a+b;
		 return sum;
	 }
	 public int minus(int a, int b) {
		 int min = a-b;
		 return min;
	 }
	 public static int mply(int a, int b) {
		 int mply = a*b;
		 return mply;
	 }
	 public double divide(int a, int b) {
		 double div = a/b;
		 return div;
	 }
}
